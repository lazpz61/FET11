// Variables = container
/*
    let - mutable/changeable
    var - mutable/changeable
    const - unmutable/unchangeable
*/

let firstName = "John"
console.log("First name: ",firstName)
firstName = "Jane"
console.log("First name: ",firstName)


var lastName = "Doe"
console.log("Last name:", lastName)
lastName = "David"
console.log("Last name:", lastName)


const birthday = "2000-01-28";
console.log("Birthday:", birthday);
