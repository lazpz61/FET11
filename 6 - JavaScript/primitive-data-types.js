//Primitive Data Types


//String: textual data enclosed in '' or " "
let firstName = 'John'
console.log("First name:", firstName)
console.log("Data Type of firstName:", typeof firstName)

// Number: represent a numerical value (30, -30, 3.14)
let age = 30
console.log("Age: ", age)
console.log("Date type of Age:", typeof age)

// Boolean: either true or false
let isAdmin = false;
console.log("Is Admin?", isAdmin);
console.log("Data Type of isAdmin:", typeof isAdmin);


// Null: Intentional absence of a value
let car = null;
console.log("Car:", car);
console.log("Data Type of car:", typeof car);
car = "Mustang";
console.log("Car:", car);


// Undefined: Unintentional absence of a value
let city;
console.log("City:", city);
console.log("Data Type of city:", typeof city);

